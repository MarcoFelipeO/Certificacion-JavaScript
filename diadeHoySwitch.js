function diaDeHoy(dia){
    switch(dia){
        case 1:
        return "Lunes";
        break;
        case 2:
        return "Martes";
        case 3:
        return "Miercoles";
        break;
        case 4:
        return "Jueves";
        break;
        case 5:
        return "Viernes";
        break;
        case 6:
        return "Sabado";
        break;
        case 0:
        return "Domingo";
        break;
    }
    return "Valor inválido";
}



